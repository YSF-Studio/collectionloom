//! Export IPC structs to TypeScript for the CollectionLoom frontend.
//!
//! Regenerate: `npm run generate:types` (from repo root)

use std::path::Path;

const OUT_DIR: &str = "../collectionloom/src/lib/generated";

fn export<T: ts_rs::TS + 'static>() {
    T::export().unwrap_or_else(|e| {
        panic!("Failed to export {}: {e}", std::any::type_name::<T>());
    });
}

fn write_index() {
    let dir = Path::new(env!("CARGO_MANIFEST_DIR")).join(OUT_DIR);
    std::fs::create_dir_all(&dir).expect("create generated dir");

    let types = [
        "DiskInfo",
        "DiskPartitionInfo",
        "ProgressState",
        "ImagingSummary",
        "WriteBlockerStatus",
        "HpaDcoReport",
        "PreflightCategory",
        "PreflightCheck",
        "PreflightReport",
        "PortableLayout",
        "PortableStatus",
        "StorageCheckReport",
        "EvidenceHashReport",
        "MobileDevice",
        "ProcessEntry",
        "EncryptionReport",
        "EncryptedPartitionInfo",
    ];

    let mut index = String::from(
        "// Auto-generated by `cargo run --bin export-types` — do not edit.\n\n",
    );
    for name in types {
        index.push_str(&format!("export type {{ {name} }} from \"./{name}\";\n"));
    }
    std::fs::write(dir.join("index.ts"), index).expect("write index.ts");
}

fn main() {
    use ysf_core::encryption_detect;
    use ysf_core::evidence_hash;
    use ysf_core::hpa_dco;
    use ysf_core::imaging;
    use ysf_core::mobile;
    use ysf_core::portable;
    use ysf_core::preflight;
    use ysf_core::progress;
    use ysf_core::snapshot;
    use ysf_core::storage_check;
    use ysf_core::write_blocker;

    export::<imaging::DiskInfo>();
    export::<imaging::PartitionInfo>();
    export::<progress::ProgressState>();
    export::<progress::ImagingSummary>();
    export::<write_blocker::WriteBlockerStatus>();
    export::<hpa_dco::HpaDcoReport>();
    export::<preflight::PreflightCategory>();
    export::<preflight::PreflightCheck>();
    export::<preflight::PreflightReport>();
    export::<portable::PortableLayout>();
    export::<portable::PortableStatus>();
    export::<storage_check::StorageCheckReport>();
    export::<evidence_hash::EvidenceHashReport>();
    export::<mobile::MobileDevice>();
    export::<snapshot::ProcessEntry>();
    export::<encryption_detect::EncryptionReport>();
    export::<encryption_detect::PartitionInfo>();

    write_index();
    eprintln!(
        "Exported {} IPC types to packages/collectionloom/src/lib/generated/",
        17
    );
}
